const admin = require('firebase-admin');
const fs = require('fs');
const path = require('path');
require('dotenv').config();

/**
 * üì• IMPORT DE LIEUX EN BATCH FIRESTORE
 *
 * Importe les lieux pour chiens r√©cup√©r√©s par fetchGooglePlaces.js
 * dans la collection Firestore 'brussels_places'
 *
 * Co√ªt : Firestore uniquement (pas d'API externe)
 */

class PlaceImporter {
    constructor() {
        this.firestore = admin.firestore();
        this.BATCH_SIZE = 500; // Limite max Firestore
        this.COLLECTION_NAME = 'brussels_places';

        this.stats = {
            total: 0,
            processed: 0,
            saved: 0,
            updated: 0,
            skipped: 0,
            errors: 0,
            byCategory: {},
            startTime: Date.now()
        };
    }

    /**
     * üîç Chargement du fichier de lieux
     */
    loadPlacesFile(filename = 'brussels_places.json') {
        const filePath = path.join(__dirname, '..', 'data', filename);

        if (!fs.existsSync(filePath)) {
            throw new Error(`Fichier non trouv√©: ${filePath}`);
        }

        console.log(`üìÇ Chargement du fichier: ${filePath}`);

        const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

        if (!data.places || !Array.isArray(data.places)) {
            throw new Error('Format de fichier invalide - lieux manquants');
        }

        console.log(`‚úÖ ${data.places.length} lieux charg√©s`);
        console.log(`üìä Source: ${data.metadata?.source || 'Inconnue'}`);
        console.log(`üìÖ R√©cup√©r√©s le: ${data.metadata?.fetchedAt || 'Inconnu'}`);
        console.log(`üí∞ Co√ªt API estim√©: ${data.metadata?.apiCostEstimate || 'Inconnu'}`);

        // Afficher les cat√©gories
        if (data.metadata?.categories) {
            console.log('\nüè∑Ô∏è CAT√âGORIES TROUV√âES:');
            data.metadata.categories.forEach(cat => {
                console.log(`   ${cat.displayName}: ${cat.count} lieux`);
            });
        }

        return data.places;
    }

    /**
     * üîß Pr√©paration des donn√©es pour Firestore
     */
    preparePlaceForFirestore(place) {
        // Validation des donn√©es obligatoires
        if (!place.id || !place.name || !place.location) {
            throw new Error(`Donn√©es obligatoires manquantes: ${place.name || 'Lieu sans nom'}`);
        }

        // G√©n√©ration de l'adresse de recherche
        const searchAddress = this.generateSearchAddress(place);

        return {
            id: place.id, // Google Place ID
            data: {
                // Identifiants
                place_id: place.id,
                name: place.name.trim(),
                type: place.type || 'unknown',
                category: place.category || 'Lieu pour chiens',

                // G√©olocalisation
                location: {
                    latitude: parseFloat(place.location.latitude),
                    longitude: parseFloat(place.location.longitude)
                },

                // Adresse
                address: place.address || '',
                searchAddress,

                // Contact
                phone: place.phone || null,
                website: place.website || null,

                // √âvaluations
                rating: place.rating || null,
                ratingsCount: place.ratingsCount || 0,

                // Informations pratiques
                openingHours: place.openingHours || [],
                priceLevel: place.priceLevel || null,

                // M√©dias
                photos: place.photos || [],

                // M√©tadonn√©es
                source: place.source || 'Google Places API',
                isActive: true,
                isDogFriendly: true, // Tous nos lieux sont dog-friendly par d√©finition
                lastFetched: place.lastFetched ? new Date(place.lastFetched) : new Date(),
                createdAt: admin.firestore.FieldValue.serverTimestamp(),
                updatedAt: admin.firestore.FieldValue.serverTimestamp()
            }
        };
    }

    /**
     * üîç G√©n√©ration d'adresse de recherche
     */
    generateSearchAddress(place) {
        const terms = [];

        if (place.name) {
            terms.push(place.name.toLowerCase());
        }

        if (place.address) {
            // Extraire commune et rue de l'adresse
            const addressParts = place.address.split(',');
            addressParts.forEach(part => {
                const cleaned = part.trim().toLowerCase();
                if (cleaned.length > 2) {
                    terms.push(cleaned);
                }
            });
        }

        if (place.category) {
            terms.push(place.category.toLowerCase());
        }

        // Mots-cl√©s par type
        const keywords = this.getKeywordsByType(place.type);
        terms.push(...keywords);

        return [...new Set(terms)].filter(term => term.length > 0);
    }

    /**
     * üè∑Ô∏è Mots-cl√©s par type de lieu
     */
    getKeywordsByType(type) {
        const keywordMap = {
            'dog_parks': ['parc', 'chien', 'dog', 'park', 'aire', 'jeux'],
            'veterinary': ['veterinaire', 'veterinary', 'clinique', 'animal', 'sante'],
            'pet_stores': ['animalerie', 'pet', 'store', 'magasin', 'animal', 'nourriture'],
            'dog_friendly_cafes': ['cafe', 'restaurant', 'dog', 'friendly', 'chien', 'accepte']
        };

        return keywordMap[type] || [];
    }

    /**
     * üì• Import principal en batches
     */
    async importToFirestore(places) {
        console.log(`\nüì• IMPORT VERS FIRESTORE`);
        console.log(`üìä ${places.length} lieux √† traiter`);

        this.stats.total = places.length;

        // V√©rifier les lieux existants
        await this.checkExistingPlaces();

        const collection = this.firestore.collection(this.COLLECTION_NAME);
        let batchCount = 0;

        // Traitement par batches
        for (let i = 0; i < places.length; i += this.BATCH_SIZE) {
            batchCount++;
            const batchPlaces = places.slice(i, i + this.BATCH_SIZE);

            await this.processBatch(collection, batchPlaces, batchCount);

            // Pause entre les batches pour √©viter les rate limits
            if (i + this.BATCH_SIZE < places.length) {
                await this.sleep(200);
            }
        }

        console.log(`\nüéâ IMPORT TERMIN√â!`);
        this.generateImportReport();
    }

    /**
     * üì¶ Traitement d'un batch
     */
    async processBatch(collection, places, batchNumber) {
        const batch = this.firestore.batch();
        let batchProcessed = 0;

        for (const place of places) {
            try {
                const prepared = this.preparePlaceForFirestore(place);

                // V√©rification basique de validit√©
                if (!this.isValidPlace(prepared.data)) {
                    this.stats.skipped++;
                    continue;
                }

                // V√©rifier si le lieu existe d√©j√†
                const docRef = collection.doc(prepared.id);
                const existingDoc = await docRef.get();

                if (existingDoc.exists) {
                    // Mise √† jour si diff√©rences
                    const existingData = existingDoc.data();
                    if (this.hasSignificantChanges(existingData, prepared.data)) {
                        batch.set(docRef, {
                            ...prepared.data,
                            createdAt: existingData.createdAt // Conserver la date de cr√©ation
                        }, { merge: true });
                        this.stats.updated++;
                    } else {
                        this.stats.skipped++;
                        continue;
                    }
                } else {
                    // Nouveau lieu
                    batch.set(docRef, prepared.data);
                    this.stats.saved++;
                }

                // Statistiques par cat√©gorie
                const category = prepared.data.type;
                this.stats.byCategory[category] = (this.stats.byCategory[category] || 0) + 1;

                batchProcessed++;
                this.stats.processed++;

            } catch (error) {
                this.stats.errors++;
                console.warn(`‚ö†Ô∏è Erreur lieu ${place.name}: ${error.message}`);
            }
        }

        try {
            if (batchProcessed > 0) {
                await batch.commit();

                const totalBatches = Math.ceil(this.stats.total / this.BATCH_SIZE);
                const progress = ((this.stats.processed / this.stats.total) * 100).toFixed(1);

                console.log(`‚úÖ Batch ${batchNumber}/${totalBatches}: ${batchProcessed} lieux trait√©s (${progress}%)`);
            }

        } catch (error) {
            this.stats.errors += batchProcessed;
            console.error(`‚ùå Erreur batch ${batchNumber}:`, error.message);
        }
    }

    /**
     * üîç V√©rification des lieux existants
     */
    async checkExistingPlaces() {
        try {
            const collection = this.firestore.collection(this.COLLECTION_NAME);
            const snapshot = await collection.count().get();
            const existingCount = snapshot.data().count;

            if (existingCount > 0) {
                console.log(`‚ö†Ô∏è ${existingCount} lieux d√©j√† pr√©sents dans la collection`);
                console.log(`üîÑ Mise √† jour des lieux modifi√©s uniquement`);
            } else {
                console.log(`‚úÖ Collection vide - import initial`);
            }

        } catch (error) {
            console.warn(`‚ö†Ô∏è Impossible de v√©rifier les lieux existants:`, error.message);
        }
    }

    /**
     * ‚úÖ Validation d'un lieu
     */
    isValidPlace(placeData) {
        return placeData.name &&
               placeData.name.length > 0 &&
               placeData.location &&
               typeof placeData.location.latitude === 'number' &&
               typeof placeData.location.longitude === 'number' &&
               placeData.location.latitude >= 50.7 &&
               placeData.location.latitude <= 51.0 &&
               placeData.location.longitude >= 4.0 &&
               placeData.location.longitude <= 4.6;
    }

    /**
     * üîÑ D√©tection de changements significatifs
     */
    hasSignificantChanges(existing, updated) {
        const significantFields = ['name', 'address', 'phone', 'website', 'rating', 'ratingsCount'];

        return significantFields.some(field => {
            const existingValue = existing[field];
            const updatedValue = updated[field];

            // Traitement sp√©cial pour les ratings
            if (field === 'rating' || field === 'ratingsCount') {
                return Math.abs((existingValue || 0) - (updatedValue || 0)) > 0.1;
            }

            return existingValue !== updatedValue;
        });
    }

    /**
     * üìä Rapport d'import
     */
    generateImportReport() {
        const duration = (Date.now() - this.stats.startTime) / 1000;

        console.log(`\nüìä RAPPORT D'IMPORT`);
        console.log(`==================`);
        console.log(`‚è±Ô∏è  Dur√©e: ${duration.toFixed(1)}s`);
        console.log(`üìä Total: ${this.stats.total} lieux`);
        console.log(`üíæ Nouveaux: ${this.stats.saved}`);
        console.log(`üîÑ Mis √† jour: ${this.stats.updated}`);
        console.log(`‚è≠Ô∏è  Ignor√©s: ${this.stats.skipped}`);
        console.log(`‚ùå Erreurs: ${this.stats.errors}`);

        console.log('\nüè∑Ô∏è PAR CAT√âGORIE:');
        Object.entries(this.stats.byCategory).forEach(([type, count]) => {
            console.log(`   ${type}: ${count} lieux`);
        });

        const successCount = this.stats.saved + this.stats.updated;
        const successRate = (successCount / this.stats.total * 100).toFixed(1);

        console.log(`\nüéØ Taux de succ√®s: ${successRate}%`);
        console.log(`‚úÖ Collection Firestore: '${this.COLLECTION_NAME}'`);
        console.log(`üêï Tous les lieux sont marqu√©s dog-friendly`);

        if (successRate < 80) {
            console.log(`\n‚ö†Ô∏è  Taux de succ√®s faible (${successRate}%)`);
            console.log(`üí° V√©rifiez les donn√©es source et la validit√© des coordonn√©es`);
        }
    }

    /**
     * üí§ Pause utilitaire
     */
    sleep(ms) {
        return new Promise(resolve => setTimeout(resolve, ms));
    }
}

/**
 * üéØ FONCTION PRINCIPALE
 */
async function main() {
    console.log('üì• IMPORT DE LIEUX EN FIRESTORE');
    console.log('===============================');
    console.log('üìÖ', new Date().toLocaleString('fr-BE'));

    // Initialisation Firebase
    if (!admin.apps.length) {
        admin.initializeApp();
    }

    const importer = new PlaceImporter();

    try {
        // 1. Chargement du fichier
        const places = importer.loadPlacesFile();

        if (places.length === 0) {
            console.error('‚ùå Aucun lieu √† importer');
            process.exit(1);
        }

        // 2. Import en Firestore
        await importer.importToFirestore(places);

        console.log('\nüéâ IMPORT R√âUSSI!');
        console.log('üí° Prochaine √©tape: npm run validate:places');

    } catch (error) {
        console.error('üí• Erreur lors de l\'import:', error.message);

        if (error.message.includes('non trouv√©')) {
            console.log('\nüí° Assurez-vous d\'avoir d\'abord r√©cup√©r√© les lieux:');
            console.log('   npm run fetch:places');
        }

        process.exit(1);
    }
}

module.exports = { PlaceImporter, main };

// Ex√©cution si appel√© directement
if (require.main === module) {
    main();
}